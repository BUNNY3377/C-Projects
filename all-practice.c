#include<stdio.h>
void main()
{
    //integer data type
    int num1 = 10;
    short num2 = 20;
    long num3 = 30;
    long long num4 = 40;

    //printing value
    printf("intger data types:\n");
    printf("int: %d\n",num1);
    printf("short: %d\n",num2);
    printf("long: %ld\n",num3);
    printf("long long :%lld\n",num4);

    return 0;
}
